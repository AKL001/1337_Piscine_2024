int	ft_sqrt(int nb)
{
	int	x;

	if (nb < 0)
	{
		return (0);
	}
	x = 0;
	while (x * x < nb)
	{
		x++;
	}
	if (x * x == nb)
	{
		return (x);
	}
	else
	{
		return (0);
	}
}
